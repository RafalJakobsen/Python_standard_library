# Math Module Part 1

import math


# Constants
print(math.pi)
print(math.e)

print(math.nan)
print(math.inf)
print(-math.inf)

# Trigonemetry

obst_direction = math.cos(math.pi / 4)
print(obst_direction)
print(math.sin(math.pi / 4))


# Ceiling and Floor

cookies = 10.3
candy = 7
print(math.ceil(cookies))
print(math.ceil(candy))

age = 47.9

print(math.floor(age))