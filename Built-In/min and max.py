# Minimum and Maximum

playerOneScore = 10
playerTwoScore = 4
print(min(playerOneScore, playerTwoScore))
print(min(0, 12, -12))

print(min("Kathryn", "Katie"))
print(min("Angela", "Bob"))

print(max(playerOneScore, playerTwoScore))
playerThreeScore = 14
print(max(playerTwoScore, playerThreeScore, playerOneScore))
print(max("Katrhin", "Katie"))